// ----------------------------------------------------------------
// Taille(bits)		:	8
// Taille(octets)	:	1
// Valeurs			:	[ 0..255 ]     
// ----------------------------------------------------------------
#define u8sod	unsigned char

// ----------------------------------------------------------------
// Taille(bits)		:	16
// Taille(octets)	:	2
// Valeurs			:	[ 0..65535 ]
// ----------------------------------------------------------------
#define u16sod	unsigned short

// ----------------------------------------------------------------
// Taille(bits)		:	32
// Taille(octets)	:	4
// Valeurs			:	[ 0..4294967295 ]
// ----------------------------------------------------------------
#define u32sod	unsigned long 
// ----------------------------------------------------------------
// Taille(bits)		:	64
// Taille(octets)	:	4
// Valeurs			:	[ 0..(2^64)-1 ]
// ----------------------------------------------------------------
#define u64sod	unsigned long long


// ===========================================
// ===========================================
// ===========================================
// ===========================================
//     DECLARATION DE TYPES SIGNES
// ===========================================
// ===========================================
// ===========================================
// ===========================================

// ----------------------------------------------------------------
// Taille(bits)		:	8
// Taille(octets)	:	1
// Valeurs			:	[ -128..+127 ]
// ----------------------------------------------------------------
#define s8sod	char

// ----------------------------------------------------------------
// Taille(bits)		:	16
// Taille(octets)	:	2
// Valeurs			:	[ -32768..+32767 ]
// ----------------------------------------------------------------
#define s16sod	short

// ----------------------------------------------------------------
// Taille(bits)		:	32
// Taille(octets)	:	4
// Valeurs			:	[ -2147483648..+2147483647 ]
// ----------------------------------------------------------------
#define s32sod	signed long

// ----------------------------------------------------------------
// Taille(bits)		:	64
// Taille(octets)	:	8
// Valeurs			:	[ -(2^63)..(2^63)-1 ]
// ----------------------------------------------------------------
#define s64sod	long long



// ----------------------------------------------------------------
// Taille(bits)		:	32
// Taille(octets)	:	4
// Valeurs			:	[ +/-1.175494E-38..+/-3.402823E+38 ]
// ----------------------------------------------------------------
#define f32sod	float


// ----------------------------------------------------------------
// Taille(bits)		:	64
// Taille(octets)	:	8
// Valeurs			:	IEEE 754-1985 double precision
// ----------------------------------------------------------------
#define f64sod	double

